			زندگي نامه دانشجوي شهيد مجيد صادقي نژاد</p>
		<p dir="RTL">
			زندگي روشني بخش وسازنده انساني بركشش و كوشش ومبارزه براي تكامل وبهتر زيستن معناي ديگر ندارد و در ساليه سرنيزه زيستن واز تمدن و ترقي اخلاقي بي نصيب بودن را نمي توان زندگي شرافتمندانه انساني ناميد.</p>
		<p dir="RTL">
			زماني كه ظلم برسرزمين ما سايه افكند ه بود در خانواده اي مذهبي در سال 1346 (ه.ش ) د رپنجمين روز ماه مبارك رمضان كودكي معصوم و پاك چشم به دنيا گشود . اسم پرمعناو زيباي مجيد را از قرآن نام گرفت . در همان اوائل زندگي مظلومانه اش نشانه هاي صدق و صفا در او ديده مي شد . مجيد پنج سال و نيم داشت كه به يكي از دبستانهاي خصوصي تهران رفت و هميشه در دروس و فعاليتهاي مذهبي پيشرو بود در سن كودكي علاقه زيادي به نماز داشت واز سن هشت سالگي نماز خواندن را شروع كرد و آن را برخود واجب مي دانست ودر هر موقعيتي كه بود آن را فراموش نمي كرد در پي همين خصوصيات بود كه بعد از دوران دبستان وراهنمائي در جستجوي دبيرستاني برآمد كه خواسته هاي او را درك كند در پي اين تلاشها با راهنمائي يكي از دوستان با دبيرستان مفيد آشنا شد . او در دبيرستان فعاليتهاي زيادي داشت واز مدرسه مفيد به عنوان يك دانشگاه كه مظهر علم و ايمان است نام مي برد .</p>
		<p dir="RTL">
			بيشتر اوقات را با دوستان در مدرسه بود ودر مواقعي كه در منزل بسر مي برد كم حرف و صبود بود در فكر آينده خود و كشورش بود به نوارهاي مذهبي علاقه خاصي داشت نوار شهيد مطهري را كه گوش مي داد تسكين اعصابش بود در گفته ها يش غرق مي شد و دردنيائي ديگري بسر مي برد بسيار به جهاد و شهادت مي انديشيد و مي گفت اين&nbsp; سعادتي است كه نصيب هر كسي نمي شود او با موفقيت دوران دبيرستان را پشت سر گذاشت ودركنكور شركت كرد او در رشته مهندسي مكانيك جامدات به انشگاه علم و صنعت راه يافت . مجيد سال دوم دانشگاه را پشت سر مي گذاشت كه ديگر زمانه و انسان درنگ نسمرد وبراي اولين بار خودرا براي جبهه آماده كرد . در چهار نوبت به جبهه رفت در نوبت اول به اتفاق عده اي از دوستان ديد و بازديد ايام عيدش را به سياحت جبهه ها اختصاص داد همان چند روز عشق اين سرزمين نور و عرفان را در دل او شعله ور ساخت اين بود كه پس از بازگشت در پادگان امام حسين نام نويسي كرد وبعد از 45 روز آموزش نظامي وده روز دوره امدادگري در بيمارستان . به جبهه اعزام شد در آن زمان در عمليات كربلاي 5 به عنوان يك امداد گر خدمت مي كرد بعد از چهار ماه و نيم كه در جبهه خود را آماده شهادت كرده بود اين فيض نصيبش نشد و به سلامت به تهران بازگشت مجيد ديد وسيعي داشت به جبهه ودانشگاه هر درسي انديشيد دنباله درس خودرا در دانشگاه ادامه داد ترمهاي خودرا يكي پس از ديگري پشت سر مي گذاشت به گفته دوستانش قلبي بزرگ داشت هيچگاه شكايت از مشكلات تعلم ودوري مسافت نمي كرد و در همه حال تابع بود . آرزوهاي دنيوي را گذران و پوچ مي دانست .مجيد د ردورن خود انقلابي داشت وسال سوم دانشگاه بود كه در سپاه پاسداران نام نويسي كرد اما كسي از اين موضوع مطلع نبود تا اينكه براي سومين بار براي ماموريتي خاص به طرف غرب روانه شد وبعد از پانزده روز دوباره به تهران بازگشت هربار مجيد به جبهه مي رفت آخرين كلامي كه بعد از خداحافظي مي گفت اين بود به كسي نگوئيد من جبهه چكاره ام وكي به جبهه رفته ام اسلام دشمن دارد شما به گفته هاي منافقان گوش ندهيد .</p>
		<p dir="RTL">
			مجيد براي چهارمين بار آما ده جبهه شد يك شب فراموش نشدني بود به خانه خبر داد كه فردا براي ماموريتي به سوي غرب مي رويم و صبح روز بعد يكي از دوستان همرزمش به دنبالش آمد براي آخرين بار خداحافظي كرد ودوان دوان به سوي ماشين سپاه كه به دنبالش آ,ده بود رفت رفتش غير از رفتن معمول انگار كه مي دانست كه اين بار به آرزويش مي رسد مجيد بعد از يك هفته كه از ماموريتش گذشته بود در روز شنبه 2 بعد ازظهر 30 آبانماه 1366 در حالي كه با فرمانده خود براي ماموريت در حال پيشروي بسوي ماوت در خاك عراق بودند گلوله اي به زمين خورد و تركش آن برقلب پاك مجيد كه اين بار ديگر خالص شده بود فرونشست و مجيد در همان لحظه اول آماده براي مهماني خدا شد وبسوي ملكوت پركشيد واز اين دنياي ظلم وجهالت كه زندگاني در آن براي مومنان به معاد دردناك وعذاب آور است ، رشته تعلق را گسست وبه جايگاهي جاودان رسيد كه خدا براي شهيدان و عده داده و خود ميزباني از ايشان را بعهده گرفته است . بعد از پنج روز پيكر پاكش به تهران منتقل شد و خانواده و همزمان پيكرش را همچون سلاح شرف بردوش تا امامزاده محمد كرج همراهي كردند . روحش شاد و راهش پور رهرو باد با اميد پيروزي نهائي اسلام بركفر جهاني</p>
