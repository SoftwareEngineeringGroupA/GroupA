		<h1 dir="RTL">
			<span style="color:#339900;">بسم الله الرحمن الرحيم</span></h1>
		<p dir="RTL">
			<span style="color:#008000;">وصیت نامه ی شهید مجید صادقی نژاد.</span></p>
		<p dir="RTL">
			خدمت برادر ودوست عزيزم ، مجيد شهيد اميدوارم حالت خوب باسد ودر جوار حضرت حق برسرسفره ابي عبدلله متنعم باشي.</p>
		<p dir="RTL">
			مجيد ! راستش وقتي تصميم گرفتم برايت نامه بنويسم ، فقط نيتم درد دل كردن بود . بعدا خواهم گفت چرا اين كار را زودتر وبراي شهداي ديگر نكردم . براي همين از مقدمات واحوال پرسي مي گذرم وسراصل مطلب مي روم .</p>
		<p dir="RTL">
			مجيد ! به ياد داري آن زماني را كه حلاجيان شهيد شد ؟ اولين شهيد دوره مان بود . بچه ها خيلي متاثر شدند . علي با خصوصياتي كه داشت با رفتنش قلب خيليها را مجروح كرد وهمان موقع بود كه امام هم به مناسبت حلول سال 65 مسله حضور در جبهه را داغ كردند.</p>
		<p dir="RTL">
			اين دو مسئله يعني شهادت علي و پيامهاي مكرر امام موجب شد تكاپوي جديدي در تعدادي از بچه ها ايجاد شود وبا اعزام دانشجويي چند نفر راهي جنگ شدند . به ياد دارم آن موقع ، آماري گرفتم كه حدود 25 نفر از هم دوره ايها جبهه بودند . در آن زمان ، امير همايون صرافي هم شهيد شد . ولي مي داني با شهادت علي چه قدر تفاوت داشت ؟ با اينكه دومين شهيد ما بود (البته به دلائلي از شهيد جهانگير مي گذرم ) وهنوز فكر فراق از علي در ذهن بچه ها بود وهنوز شهادت او را هضم نكرده بودند وعليرغم اينكه بچه ها مي دانستند عده زيادي از دوستانشان وهمكلاسيهايشان وهم دوره ايهايشان در جبهه بسر مي برند وامكان شركت در تشييع جنازه امير برايشان نيست ، عده اي گفتند : كار دارند ونمي توانندد در تشييع جنازه شركت كنند واينگونه توجيه مي كردند كه خود امير هم راضي تر است كه آنها درسشان را بخوانند وبعد در مراسم ختم شركت كنند !</p>
		<p dir="RTL">
			عده اي از آن واقعه خم به ابرو هم نياوردند واصلا هيچ نفهميدند كه &laquo;امير رفت &raquo; يعني چه ! اگر مي فهميدند ، لااقل در تشييع جنازه اش شركت مي كردند ويا در تنظيم مراسم ختم و تجليل از مقام شهيد ، با حضور خود دل خانواده اش را گرم مي كردند وبه آنها تبريك و تسليت مي گفتند .</p>
		<p dir="RTL">
			از آن جريان چند ماهي گذشت تا اينكه خبرشهادت وجا ماندن جنازه غلامرضا رمضان زاده (محمود ) به گوش گنهكارمان رسيد . فكر مي كني راجع به اين مصيبت چند نفر فكر كردند ؟ چند نفر احساس كردند كسي را از دست داده اند كه <span dir="LTR">&hellip;</span>.؟</p>
		<p dir="RTL">
			هر چه فكر مي كنم نمي دانم كدام خصوصيتش را بگويم . قلمم نمي تواند حتي آغاز به نوشتن فضائلش كند .</p>
		<p dir="RTL">
			به ياد داري آخرين نامه اش را كه براي بچه هاي جلسه نوشته بود ، همه آن را خواندند ؟ ولي چه فايده ! اي كاش آن را نمي خواندند ونمي دانستند محمود چه دردهايي دارد .</p>
		<p dir="RTL">
			دوباره قضيه خوابيد . همانهايي كه جبهه مي رفتند باز هم رفتند و آنهايي هم كه نمي رفتند <span dir="LTR">&hellip;</span></p>
		<p dir="RTL">
			يادت هست امام در آن مقطع جنگ چگونه نداي &laquo;هل من ناصر ينصرني &raquo; داد ؟ هرگز فراموش نخواهم كرد آن جمله را كه فرمود : &laquo; من از جوانان مي خواهم كه به سپاه مهدي (عج) بپيوندند و <span dir="LTR">&hellip;</span>&raquo;</p>
		<p dir="RTL">
			آن زمان بود كه خود تو در جبهه بودي . آن زمان حرفي از اعزام نوبه اي نبود . آن زمان حرفي از دانشجو و غير دانشجو نبود . آن زمان حرفي از اعزام اجباري بود . ولي چه شد ؟ هيچ . آنهايي كه قبلا هم مي رفتند رفتند و باقي <span dir="LTR">&hellip;</span>واسماعيل شيرازي هم شهيد شد . ولي دوباره شهادت بدون تشييع جنازه . ولي چه كسي فهميد فرق شهادت با تشييع وشهادت بدون تشييع چست ؟</p>
		<p dir="RTL">
			اصلا ديگر چه كسي به شهدتها اهميت مي داد ؟</p>
		<p dir="RTL">
			فقط به بچه ها بگو فلان روز منزل فلان شهيد . اگر كاري نداشته باشند ! با كله مي آيند . اگر شام باشد كه سعي مي كنند كارهايشان را زودتر انجام دهند كه برسند .</p>
		<p dir="RTL">
			مجيد ! خودت مي داني منظورم از بچه هاي كيست . همانهايي كه فكر نمي كنند برمادري كه جنازه پسرش نيامده چه مي گذرد . تنها به فكر اين هستند كه اگر خودشان بروند ، مادرشان چه حالي مي شود . چند سال است مادراني اصلا نمي دانند فرزندشان چه شده ! وبعضي ها به بهانه مسائل خانوادگي چند سال از جنگ مي گذرد وهنوز نمي دانند خمپاره چه صدايي دارد . مفقود چه معنايي دارد ، اسير چه حالي دارد ، مجروح چه دردي دارد ! فقط در فكر اين هستند كه حال مادرشان بهم نخورد ،</p>
		<p dir="RTL">
			پدرشان دوري شان را تحمل نكند ، خودشان از درس عقب نيفتند و <span dir="LTR">&hellip;</span>!</p>
		<p dir="RTL">
			ولي مجيد تا آن موقع كسي حرفي نمي زد ، فقط نمي رفتند . ديگر كسي هم به آنها چيزي نمي گفت . شهر ، در امن و امان بود . چند نفر از دوستان همرزمت تصميم گرفتند ديگر رابطه اشان را كم كنند ، شايد تغييري در امنيت شهر حاصل شود ولي بندگان خدا محكوم شدند . دوستانشان هم كه بهشان مي رسيدند ،مي گفتند : كارتان غلط است بايد سعه صدر داشت . بايد احتمال داد دارند درست عمل مي كنند ، بايد اتمال داد نبايد جبهه بروند !</p>
		<p dir="RTL">
			آن بيچاره ها هم ديگر زدند به رگ بي خيالي و كاري به كار جنگ نرفته ها نداشتند . ولي بازچه فايده ؟</p>
		<p dir="RTL">
			گذشت تا اينكه بلورچي وكريميان وصالحي وكاظمي وفيض و <span dir="LTR">&hellip;</span> هم شهيد شدند . يكي از ديگري گل تر يكي از ديگري عاقل تر . يكي از ديگري با سوادتر . بلور چي كه تنها فرزند پسر مادرش بود و مادرش هم تنها سرپرست او . كاظمي هم با كوهي از مشكلات خانوادگي . صالحي با مسئوليتي برگرفته از حضور مداومش از اول جنگ . كريميان وفيض با آن متانت وبينايي خاص خود و ديگران هركدام با خصوصياتي كه يكي از آنها هم براي روشنايي راهمان ما را كافي بود . ولي چه شد ؟ هيچ .</p>
		<p dir="RTL">
			اين بار از هيچ هم بدتر . اين بار در جواب سوال مگر اينها مشكلات نداشتند ورفتند ؟ سكوت نبود ! نرفتن تنها نبود ! بلكه&nbsp; جوابي بود . گفته شد : مگر هر كاري ديگران كردند ما هم بايد بكنيم ؟!؟ هر كسي وظيفه اي دارد و<span dir="LTR">&hellip;</span></p>
		<p dir="RTL">
			خوب مجيد ! ناراحت نباش . دير يا زود بايد چنين جوابي مي گرفتيم . ولي اي كاش به همين جا ختم مي شد . نه . ادامه داشت وبايد بگويم تازه شروع شد . جريانات رسيد به اينجا كه اعزامها نوبه اي شد . تكليف دانشجوها تا حدودي معلوم شد .</p>
		<p dir="RTL">
			نرفتها موجه شد (لااقل براي مدتها) . تا اينكهدهه شهداي مدرسه آغاز شد . تو كه خودت مسئولش بودي بهتر مي داني چند نفر همكاري مي كردند . اصلا در نظر خيلي از بچه ها كه ديگر به اين كارها ، به اين تجليلها ، به اين ياد آوريها ، به اين بازنگريها ، به اين زنده نگهداشتنها ، به اين ابراز ارادتها به خانواده شهد ا، به اين الگونمايي زندگي شهدا ، به اين هدفداري راه شهدا اعتقادي ندارند ! ديگر فكر مي كنند چهار تا كتاب خوانده اند ويا چهار تا كلاس رفته اند ديگر نيازي به اين چيزها ندارند !</p>
		<p dir="RTL">
			بيچاره نمي داند خودش نيازمند شركت دراين فعاليتهاست . قضيه را به ديد فايده رساندن نگاه مي كند . فكر مي كند اگر همكاري نكند ضرري به كار مي رسد ويا اگر كمك كند خدمتي ضرري به كار مي رسد ويا اگر كمك كند خدمتي به شهيد كرده . بدبخت نمي داند در وهله اول ، خودش است كه از شركت دراين برنامه ها استفاده بايد بكند .</p>
		<p dir="RTL">
			خودش است كه بايد خاطره اين شهدا را درذهنش زنده نگهدارد و از اعمال ناشايست واز نافرمانيهايي كه از امام كرده پشيمان شود .</p>
		<p dir="RTL">
			بگذريم ، نيامدند . شركت نكردند . همان طور كه كم كم سرقبر شهدا هم ديگر نخواهند رفت . حالا سرقبرشان نرفتند به درك ! راهشان را زير سوال نبرند . آري مجيد ! دارد اينجور مي شود ؟!</p>
		<p dir="RTL">
			خودت كه بهتر مي داني . بعد از شهادت خودت ديدي چه شد ؟ در جلسه وقت براي خواندن قرآن برايت نبود . در صورتي كه براي حلاجيان دوبار قرآن خوانده شد ، براي صرافي كمتر ، براي شيرازي ورمضانزاده كمتر ، براي تو هم كه هيچ . اصلا جلسه وقتش با ارزش تر از اين است كه براي شهيدي مثل تو قرآن خوانده شود ؟!<span dir="LTR">&hellip;</span></p>
