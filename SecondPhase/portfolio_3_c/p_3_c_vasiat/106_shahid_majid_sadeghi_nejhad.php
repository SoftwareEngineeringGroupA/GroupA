			بسم الله الرحمن الرحيم . رب اشرح لي صدري و يسرلي امري واحلل عقده من لساني يفقهوا قولي .</p>
		<p dir="RTL">
			پروردگار من ، سينه ام را گشاده گردان وامررا برمن آسان گردان و گره از زبانم بگشاي تا سخنم را بپذيرند .</p>
		<p dir="RTL">
			با سلام به امام زمان و نائب برحقش امام خميني .</p>
		<p dir="RTL">
			انشاء&zwnj;ا<span dir="LTR">&hellip;</span> الان كه نامه مرا مي خواني حالت خوب است وهيچگونه ناراحتي نداري وان شاء ا<span dir="LTR">&hellip;</span> كه خداوند نيز هم اكنون از همه ما و كاري كه انجام مي دهيم راضي است وما را از گناهكاران قرار نداده است . و انشاء&zwnj;ا<span dir="LTR">&hellip;</span> با داشتن يك عقيده و هدف بتوانيم بهتر در راهي كهبايد برويم قدرم برداريم كه انما الحيوه عقيده و جهاد . ويكي از معصومين چه خوب بيان نموده است كه زندگي فقط عقيده و جهاد در راه آنست وآنچه را كه انجام مي دهيم واقعا اگر در نهايت منتهي به اين مسئله نشود براي ما فائده ندارد و در نهايت چيزي از آن براي ما نمي ماند .</p>
		<p dir="RTL">
			برادرم ! حال كه رفته اي ودرس مي خواني سعي كن كه درس خواندنت نيز در راه خدا باشد وچنان شو كه لحظه اي از خدا غافل نباشي ودر سرهر كلاس درس علت اينكه به اينجا آمده اي و درس مي خواني يادآور شود . چرا كه زندگي عقيده و جهاد در راه آن است واگر اين درس خواندن براي عقيده و جهاد در راه آن نباشد ودر راه رضاي خدا نباشد مقبول نيست وبايد در نهايت اجرت را از كسي ديگر بگيري . هنگامي كه اين مسائل را از ذهن خود مي گذراندم ديدم مي خواهم چيزيهايي بگويم كه خودت به نحو احسن مي داني و با در نظر گرفتن تمام اينها كارهايت را انجام مي دهي وليكن ديدم خودتذكري است ، انشاء&zwnj;ا<span dir="LTR">&hellip;</span> كه تو و همچنين خودم را نفعي عايد آيد . و انشا ء ا<span dir="LTR">&hellip;</span> كه خدا بر ما منت گذارد واز اصحاب خودش قرمان دهد وما را در انجام كارهايي كه رضايت او در آن است موفقمان دارد .</p>
