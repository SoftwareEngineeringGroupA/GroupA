			مي خواستم بگويم در زندگي خودرحالت سوز داشتن را براي خودنگه دار . من هم اكنون كه اينجا نشسته ام هجده سال تمام دارم و مقداري از سنم گذشته است و آرزوهايي داشته ام كه مقداري به آنها رسيده ام ، چيزهايي براي خود در ذهن مجسم مي كرده ام و بخودمي گفته ام اگر من به آن دست يابم ديگر باكي ندارم ، ديگر ناراحتي ندارم ، ديگر به كمال رسيده ام ولي همه اينها چيزهايي بوده اند كه بعد از مدتي براي انسان يكنواخت شده وآدمي از آن سير مي شود ومي بيند كه خير ، گمشده اصلي خود را هنوز نيافته است و تمام اينهااسباب بازيهايي بوده اند . بنابراين انسان بايد به حال ديگري چنگ بزند وبه چيزي ديگر دل ببندد كه زودگذر نباشد و فنا پذير نباشد وواقعا آدمي را قانع گرداند و آدمي در تكاپو براي رسيدن به آن باشد وبراي رسيدن به آن بايد داراي سوزي باشد وبايد همواره عاشق باشد . همواره در راه رسيدن به معشوق خودتلاش كند وبراي اينكه آدمي سوز داشته باشد بايد از طريق صحيح اين كار را انجام دهد وبا وسيله قرار دادن اولياء&zwnj; اطهار در راه رسيدن به خدا كار كند . و خدا پيامبران واولياء&zwnj;را فرستاد تا آدمي را هدايت به راه او كنند و او را كمك كنند پس سعي كن عاشق باشي و به اين عاشقي عشق بورزي وهر روزت بهتر از روز قبل باشد وبه خدا نزديگتر شده باشي وباز بگويم در راه رسيدن به كمال مطلوب ، انبياء&zwnj;و اوصيا ء را فراموش نكني چرا كه آنها وسيله هستند براي رسيدن به خدا و در نمازهايت از خدا توفيق اينكه حب اولياء را در دلمان جاي دهد بخواه و با بهره گيري از كتابهايي چون صحيفه سجاديه سعي كن براي خود حالي پيدا كني و چنان كن كه خدا از تو راضي باشد و خلاصه تو را به تقواي خدا سفارش مي كنم واينكه اعتماد به نفس داشته باشي كه انشاء&zwnj;ا<span dir="LTR">&hellip;</span> تحولي در افراد همراه خود بدهي و آنها را راهنمايي كني و به تو سفارش مي كنم هنگامي كه مشكلات به تو رو مي آورد ولحظات حساس مي گردد از قرآن مدد بخواهي وبا پناه بردن به آن مقداري به خود آرامش دهي كه الابذكرالله تطمئن القلوب .</p>
		<p dir="RTL">
			انشاء&zwnj;ا<span dir="LTR">&hellip;</span> اين دوران تمام خواهد شد و همه براي خدمت به كشور و خدمت به اسلام آماده خواهيم شد و شروع به فعاليت در راه سازندگي كشور مي كنيم و خدايا از تو مي خواهيم كه هدف از درس خواندن ما را جز اين قرار مده . چرا كه اگر جز اين باشد عمرمان را تلف كرده ايم . خدايا مارا از كساني كه در روز قيامت دست خالي هستند قرارمده . مي خواستم در مورد دعاها با تو صحبت كنم واينكه واقعا دعا كن چرا كه دعا سلاح مومن است وواقعا چه حرفي است كه اگر در آن غور كنيم به خيلي از چيزها دست خواهيم يافت . دعا براي پيروزي رزمندگان بكن با قلبي خاشع و خاضع . دعا براي ا مام امت بكن . دعا براي عاقبت به خير شدن بكن دعا براي اينكه خداوند از سرتقصيرات ما بگذرد بكن . دعا براي اينكه خدا ما را به آنچه كه خودش به آن رضايت دارد هدايت فرمايد و ما را كمك كند تا به آن برسيم و خلاصه براي پدر و مادرت و خويشان ودوستان خود دعا كن .</p>
